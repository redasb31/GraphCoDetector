#include <stdio.h>

int main() {
  // Declare variables to store the input values
  int A, B;

  // Read the input values from the user
  scanf("%d %d", &A, &B);

  // Calculate the product of A and B
  int product = A * B;

  // Print the product to the console
  printf("%d\n", product);

  return 0;
}